//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
LocoCrane *loadLocoCrane(QString lib_path)
{
    LocoCrane *crane = nullptr;

    QLibrary lib(lib_path);

    if (lib.load())
    {
        GetLocoCrane getLocoCrane = reinterpret_cast<GetLocoCrane>(lib.resolve("getLocoCrane"));

        if (getLocoCrane)
        {
            crane = getLocoCrane();
        }
    }

    return crane;
}
